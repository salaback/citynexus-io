    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $task = $request->all();
        if(isset($task['due_at'])) $task['due_at'] = strtotime($request['due_at']);
        $task = Task::create($task);

        if($task->assignee != null) $task->assignee->notify(new AssignedNewTask($task));

        return view('snipits.tasks._task', compact('task'));
    }

